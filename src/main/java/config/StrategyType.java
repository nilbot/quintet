package config;

/**
 * StrategyType: Const flag for different solving strategies
 */
public enum StrategyType {
    /**
     * logic.BruteForce applies total permutation
     */
    BruteForce,
    /**
     * SimulatedAnnealing applies simulated annealing
     */
    SimulatedAnnealing,
    /**
     * GeneticAlgorithm applies genetic algorithm
     */
    GeneticAlgorithm,
}
